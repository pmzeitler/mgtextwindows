
namespace net.PhoebeZeitler.TextWindowSystem
{
    /// <summary>
    /// A convenience class that standardizes all possible user interactions with an active window.
    /// </summary>
    public enum WindowInteractions
    {
        // Confirm/advance
        OK,
        // Cancel one
        CANCEL,
        // Close all windows
        CANCEL_ALL,
        // d-pad/arrow up
        CURSOR_UP,
        // d-pad/arrow down
        CURSOR_DOWN,
        // d-pad/arrow left
        CURSOR_LEFT,
        // d-pad/arrow right
        CURSOR_RIGHT

    }
}
